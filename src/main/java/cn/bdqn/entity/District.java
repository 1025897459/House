package cn.bdqn.entity;

/**
 * 区县类
 */
public class District {
    private int id;//区县id;
    private String name;//区县名称

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
